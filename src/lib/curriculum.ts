export type SubjectChoice = (typeof SUBJECT_CHOICES)[number];
export type TopicChoice = keyof typeof TOPIC_TO_SUBJECT;
export type ConceptChoice = keyof typeof CANONICAL_CONCEPT_TO_TOPIC;

export const DEFAULT_SUBJECT: SubjectChoice = 'math';
export const DEFAULT_TOPIC: TopicChoice = 'linear_algebra';
export const DEFAULT_CONCEPT: ConceptChoice = 'vector_operations';

export const TOPIC_CHOICES = Object.keys(TOPIC_TO_SUBJECT) as TopicChoice[];
export const CONCEPT_CHOICES = Object.keys(CANONICAL_CONCEPT_TO_TOPIC) as ConceptChoice[];

type LessonMetadataInput = {
  subject?: string | null;
  topic?: string | null;
  concept?: string | null;
};

function resolveConceptChoice(value: string | null | undefined): ConceptChoice | undefined {
  if (!value) {
    return undefined;
  }

  if (value in LEGACY_CONCEPT_TO_CANONICAL) {
    return LEGACY_CONCEPT_TO_CANONICAL[value as keyof typeof LEGACY_CONCEPT_TO_CANONICAL] as ConceptChoice;
  }

  if (value in CANONICAL_CONCEPT_TO_TOPIC) {
    return value as ConceptChoice;
  }

  return undefined;
}

export function normalizeCurriculumSelection(input: LessonMetadataInput = {}) {
  const subject = (input.subject || DEFAULT_SUBJECT) as SubjectChoice;
  const topic = (input.topic || DEFAULT_TOPIC) as TopicChoice;
  const concept = resolveConceptChoice(input.concept) ?? DEFAULT_CONCEPT;

  if (!SUBJECT_CHOICES.includes(subject)) {
    return {
      subject: DEFAULT_SUBJECT,
      topic: DEFAULT_TOPIC,
      concept: DEFAULT_CONCEPT,
    };
  }

  if (!(topic in TOPIC_TO_SUBJECT) || TOPIC_TO_SUBJECT[topic] !== subject) {
    return {
      subject,
      topic: DEFAULT_TOPIC,
      concept: DEFAULT_CONCEPT,
    };
  }

  if (!(concept in CANONICAL_CONCEPT_TO_TOPIC) || CANONICAL_CONCEPT_TO_TOPIC[concept] !== topic) {
    return {
      subject,
      topic,
      concept: DEFAULT_CONCEPT,
    };
  }

  return { subject, topic, concept };
}

export function topicsForSubject(subject: string) {
  return TOPIC_CHOICES.filter((topic) => TOPIC_TO_SUBJECT[topic] === subject);
}

export function conceptsForTopic(topic: string) {
  return CONCEPT_CHOICES.filter((concept) => CANONICAL_CONCEPT_TO_TOPIC[concept] === topic);
}

