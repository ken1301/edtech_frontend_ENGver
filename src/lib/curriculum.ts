function formatFallbackLabel(value: string) {
  return value
    .replace(/_/g, ' ')
    .replace(/\b\w/g, (character) => character.toUpperCase());
}

export function translateSubject(val: string) { return SUBJECT_TRANSLATIONS[val as SubjectChoice] || val; }
export function translateTopic(val: string) { return TOPIC_TRANSLATIONS[val as TopicChoice] || val; }
export function translateConcept(val: string) {
  const canonicalConcept = resolveConceptChoice(val);
  if (!canonicalConcept) {
    return val;
  }

  return CONCEPT_TRANSLATIONS[canonicalConcept] || formatFallbackLabel(canonicalConcept);
}
